// operators in javascript:
// - arithmetic
// - assignment
// - comparison
// - logical
// - bitwise

// arithmetic operators

let x = 10;
let y = 3;

console.log(x + y); // addition
console.log(x - y); // substraction
console.log(x * y); // multiplication
console.log(x / y); // division
console.log(x % y); // remainder of division
console.log(x ** y); // exponiation

// increment (++)

console.log(x);
console.log(++x);
console.log(x);
console.log(x++);
console.log(x);

// decrement (--)

console.log(x);
console.log(--x);
console.log(x);
console.log(x--);
console.log(x);
